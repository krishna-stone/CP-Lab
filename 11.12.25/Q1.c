#include <stdio.h>

int main() {
    int a = 10;
    int b = 20;

    int sum = a + b;
    int difference = a - b;
    int product = a * b;
    int division_int = a / b;

    printf("Sum is %d\n", sum);
    printf("Difference is %d\n", difference);
    printf("Product is %d\n", product);
    printf("Integer Division is %d\n", division_int);

    return 0;
}

